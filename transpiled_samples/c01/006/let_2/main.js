// 数値の変数同士の足し算
var number1 = 10;
var number2 = 20;
var sum = number1 + number2;
console.log(sum); // 結果：30

// 文字列の変数を結合する
var familyName = '鈴木';
var firstName = '太郎';
var fullName = familyName + firstName;
console.log(fullName); // 結果：'鈴木太郎'

// 変数を別の変数に代入する
var value1 = 100;
var value2 = value1;
console.log(value2); // 結果：100（value1と同じ値）